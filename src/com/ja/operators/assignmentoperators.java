package com.ja.operators;

public class assignmentoperators {

    public static void main(String[] args) {
        int a = 8;
        System.out.println("Imitial a:" + a);
        a += 8;
        System.out.println("after += 8: " + a);
        a -= 4;
        System.out.println("after -= 4: " + a);
        a *= 9;
        System.out.println("after *= 9: " + a);
        a /= 7;
        System.out.println("after /= 7: " + a);
    }
}


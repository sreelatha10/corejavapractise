package com.ja.conditions;

public class ifelseif2 {
    public static void main(String[] args) {
        int num = 5;
        if (num <= 8) {
            System.out.println("num is less or equal to 8");
        } else if (num <= 9) {
            System.out.println("num is less or equal to 9");
        } else if (num <= 0) {
            System.out.println("num is less or equal to 0");
        } else {
            System.out.println("any num equal or not" +
                    "");
        }
    }
}
